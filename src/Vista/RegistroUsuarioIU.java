    public void ListarDatos(){
        Usuario = db.ListUsuarios();
        DefaultTableModel tb = (DefaultTableModel)tablaUsuario.getModel();
        for (Usuario user: Usuario ) {
            tb.addRow(new Object[]{user.getDNI(),user.getNombre(),user.getTelefono(),user.getCorreo(),user.getSegmento_social(),user.getCodigo_cliente()});
        }
    }
    public void LimpiarFormulario(){
        DefaultTableModel tb = (DefaultTableModel)tablaUsuario.getModel();
        for (int i = tb.getRowCount()-1; i>=0; i--) {
            tb.removeRow(i);
        }
    }
    public void limpiarTablaUsuario() {
        for (int i = modeloTablaUsuario.getRowCount() - 1; i >= 0; i--) {
            modeloTablaUsuario.removeRow(i);
        }
    }

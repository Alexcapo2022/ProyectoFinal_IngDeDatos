    private void btnRegistrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarUsuarioActionPerformed
        
        int DNI = Integer.valueOf(txtDNI.getText());
        String Nombre = txtNombre.getText();
        int Telefono = Integer.valueOf(txtTelefono.getText());
        String Correo = txtCorreo.getText();
        String SegSocial = txtSegSocial.getText();
        String CodCliente = txtCodCliente.getText();
        
        Usuario objUsuario = new Usuario(DNI,Nombre,Telefono,Correo,SegSocial,CodCliente);
        try {
            GestorUsuario.registrarUsuario(objUsuario);
            JOptionPane.showMessageDialog(rootPane, "Se agrego con Exito..");
            txtDNI.setText("");
            txtNombre.setText("");
            txtTelefono.setText("");
            txtCorreo.setText("");
            txtSegSocial.setText("");
            txtCodCliente.setText("");
            txtDNI.requestFocus();
            txtTelefono.requestFocus();
        } catch (Exception ex) {
            Logger.getLogger(RegistroUsuarioIU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegistrarUsuarioActionPerformed


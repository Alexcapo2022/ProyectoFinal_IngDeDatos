    public void actualizarTablaUsuario(){
        limpiarTablaUsuario();
        for (int i = 0; i < gestorUsuario.longitud(); i++) {
            Usuario usuario = gestorUsuario.iesimo(i);
            String[] fila = new String[6];
            int DNI= usuario.getDNI();
            int Telefono= usuario.getTelefono();
            
            fila[0] = String.valueOf(DNI);
            fila[1] = usuario.getNombre();
            fila[2] = String.valueOf(Telefono);
            fila[3] = usuario.getCorreo();
            fila[4] = usuario.getSegmento_social();
            fila[5] = usuario.getCodigo_cliente();
            
            modeloTablaUsuario.addRow(fila);
            
        }
    }


    public void HabilitarBoton(){
        
        if (!txtDniAsesor.getText().isEmpty()
                && !txtCorreoE.getText().isEmpty()
                && !txtCelular.getText().isEmpty()
                && !txtNombreAsesor.getText().isEmpty()
                && !txtUsuario.getText().isEmpty()
                && !txtContra.getText().isEmpty()
                ) {
            btnRegistrarAsesor.setEnabled(true);
            
        }else{
            btnRegistrarAsesor.setEnabled(false);
            }
        }

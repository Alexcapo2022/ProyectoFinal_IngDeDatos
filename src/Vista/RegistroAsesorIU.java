    public void ListarDatos(){
        Asesor = db.ListAsesores();
        DefaultTableModel tb = (DefaultTableModel)tablaAsesor.getModel();
        for (Asesor user: Asesor ) {
            tb.addRow(new Object[]{user.getDNI_ASESOR(),user.getCorreoEmpresa(),user.getCelAsesor(),user.getNombreAsesor(),user.getUsuario(),user.getContraseña()});
        }
    }
    public void LimpiarFormulario(){
        DefaultTableModel tb = (DefaultTableModel)tablaAsesor.getModel();
        for (int i = tb.getRowCount()-1; i>=0; i--) {
            tb.removeRow(i);
        }
    }
    public void actualizarTablaAsesor(){
      limpiarTablaAsesor();
        for (int i = 0; i < gestorAsesor.longitud(); i++) {
            Asesor asesor = gestorAsesor.iesimo(i);
            String[] fila = new String [6];
            int DNI= asesor.getDNI_ASESOR();
            int cel= asesor.getCelAsesor();
            
            fila[0] = String.valueOf(DNI);
            fila[1] = asesor.getCorreoEmpresa();
            fila[2] = String.valueOf(cel);
            fila[3] = asesor.getNombreAsesor();
            fila[4] = asesor.getUsuario();
            fila[5] = asesor.getContraseña();
            
            modeloTablaAsesor.addRow(fila);
        }
    }


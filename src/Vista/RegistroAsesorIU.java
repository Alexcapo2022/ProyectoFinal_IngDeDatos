    private void btnRegistrarAsesorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarAsesorActionPerformed

        int DNI = Integer.valueOf(txtDniAsesor.getText());
        String correo = txtCorreoE.getText();
        int Telefono = Integer.valueOf(txtCelular.getText());
        String nombre =txtNombreAsesor.getText();
        String Usuario = txtUsuario.getText();
        String Contraseña = txtContra.getText();

        Asesor objAsesor = new Asesor(DNI,correo,Telefono,nombre,Usuario,Contraseña);
        try {
            GestorAsesor.registrarAsesor(objAsesor);
            JOptionPane.showMessageDialog(rootPane, "Se agrego con Exito..");
            txtDniAsesor.setText("");
            txtCorreoE.setText("");
            txtCelular.setText("");
            txtNombreAsesor.setText("");
            txtUsuario.setText("");
            txtContra.setText("");
            txtDniAsesor.requestFocus();
            txtCelular.requestFocus();
        } catch (Exception ex) {
            Logger.getLogger(RegistroUsuarioIU.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRegistrarAsesorActionPerformed


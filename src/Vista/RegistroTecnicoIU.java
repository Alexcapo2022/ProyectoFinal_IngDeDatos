    public void HabilitarBoton(){
        
        if (!txtDniTecnico.getText().isEmpty()
                && !txtNombreTec.getText().isEmpty()
                && !txtCelular.getText().isEmpty()
                && !txtCompania.getText().isEmpty()
                && !txtEspecializacion.getText().isEmpty()
                ) {
            btnRegistarTec.setEnabled(true);
            
        }else{
            btnRegistarTec.setEnabled(false);
            }
        }

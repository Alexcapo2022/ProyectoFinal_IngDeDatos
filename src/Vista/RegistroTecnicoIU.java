    public void actualizarTablaTecnico(){
        limpiarTablaTecnico();
        for (int i = 0; i < gestorTecnico.longitud(); i++) {
            Tecnico tecnico = gestorTecnico.iesimo(i);
            String[] fila = new String [5];
            int DNI = tecnico.getDNI_TECNICO();
            int cel = tecnico.getCelTecnico();
            
            fila[0] = String.valueOf(DNI);
            fila[1] = tecnico.getNombreTecnico();
            fila[2] = String.valueOf(cel);
            fila[3] = tecnico.getCompañia();
            fila[4] = tecnico.getEspecialización();
            
            modeloTablaTecnico.addRow(fila);
        }
    }
    


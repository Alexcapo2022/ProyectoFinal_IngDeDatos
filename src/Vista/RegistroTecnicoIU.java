    public void ListarDatos(){
        Tecnico = db.ListTecnicos();
        DefaultTableModel tb = (DefaultTableModel)tablaTecnico.getModel();
        for (Tecnico user: Tecnico ) {
            tb.addRow(new Object[]{user.getDNI_TECNICO(),user.getNombreTecnico(),user.getCelTecnico(),user.getCompañia(),user.getEspecialización()});
        }
    }
    public void LimpiarFormulario(){
        DefaultTableModel tb = (DefaultTableModel)tablaTecnico.getModel();
        for (int i = tb.getRowCount()-1; i>=0; i--) {
            tb.removeRow(i);
        }
    }
    public void limpiarTablaTecnico() {
        for (int i = modeloTablaTecnico.getRowCount() - 1; i >= 0; i--) {
            modeloTablaTecnico.removeRow(i);
        }
    }

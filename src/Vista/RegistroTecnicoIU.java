    private void btnRegistarTecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistarTecActionPerformed
        // TODO add your handling code here:
        int DNI = Integer.valueOf(txtDniTecnico.getText());
        String Nombre = txtNombreTec.getText();
        int Cel = Integer.valueOf(txtCelular.getText());
        String compania = txtCompania.getText();
        String Especializacion = txtEspecializacion.getText();
        
        Tecnico objTecnico = new Tecnico (DNI,Nombre,Cel,compania,Especializacion);
        try {
            GestorTecnico.registrarTecnico(objTecnico);
            JOptionPane.showMessageDialog(rootPane, "Se agrego con Exito..");
            txtDniTecnico.setText("");
            txtNombreTec.setText("");
            txtCelular.setText("");
            txtCompania.setText("");
            txtEspecializacion.setText("");
            txtDniTecnico.requestFocus();
            txtCelular.requestFocus();
        } catch (Exception ex) {
            Logger.getLogger(RegistroUsuarioIU.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnRegistarTecActionPerformed

